    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $req)
    {
        $user = $user = Auth::user();
        $name = $user->name;
        $title = $req->input("title");
        $desc = $req->input("desc");
        $post = new Post();
        $post->title = $title;
        $post->desc = $desc;
        $post->user = $name;
        $post->save();
        return redirect('/post');
    }

